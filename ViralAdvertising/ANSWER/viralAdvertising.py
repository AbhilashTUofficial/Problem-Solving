def viralAdvertising(n):
    s,l,c=5,0,0
    for _ in range(n):
        l=math.floor(s/2)
        c+=l
        s=l*3
    return c


if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    n = int(input().strip())

    result = viralAdvertising(n)

    fptr.write(str(result) + '\n')

    fptr.close()
